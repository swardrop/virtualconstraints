function [t1dd, t2dd] = testsolve()
syms t1dd t1d m1 l1 I1
syms t2dd t2d m2 l2 I2
syms c1 s1 c2 s2 c12
syms T1 T2 g

[t1dd, t2dd] = solve(...
    T1 == t1dd*(I1 + I2 + m2*(l1^2 + 0.25*l2^2 + l1*l2*c2)) - ...
    t1d*t2d*(m2*l1*l2*s2) + t2dd*(I2 + 0.5*m2*(0.5*l2^2 + l1*l2*c2)) ...
    - t2d^2*(0.5*m2*l1*l2*s2) + 0.5*l1*c1*m1*g + m2*g*(l1*c1 + 0.5*l2*c12), ...
    ...
    T2 == t2dd*(I2 + 0.25*m2*l2^2) + ...
    t1dd*(I2 + 0.5*m2*(0.5*l2^2 + l1*l2*c2)) + t1d^2*(0.5*m2*l1*l2*s2) ...
    + 0.5*m2*g*l2*c12, ...
    ...
    t1dd, t2dd);
end